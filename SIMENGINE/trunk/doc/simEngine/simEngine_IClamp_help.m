%% IClamp - A current clamp point process for a Neuron model
%
%% Description
% The <matlab:doc('IClamp') IClamp> class is instantiated whenever the
% <matlab:doc('Neuron.IClamp') Neuron/IClamp> method is called.  It creates
% a current clamp point process which creates a current input into the
% point specified on the section.
%
%% Properties
%
% * del - deletion time before the current clamp begins
% * dur - duration of the curent clamp
% * amp - amplitude of the current clamp
