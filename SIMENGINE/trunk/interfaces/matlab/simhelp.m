function simhelp
% SIMHELP provides info on using the Simatra simEngine Dynamical
% System Compiler in the Matlab environment
%
% Included commands:
%    SIMEX - executes the simEngine compiler using user supplied
%    input values (running with no arguments brings up a graphical
%    user interface)
%    
%    SIMSWEEP - executes the simEngine compiler multiple times
%    across a linear sweep of parameter space (running with no
%    arguments brings up a graphical user interface)
%
%    SIMSENSITIVITY - executes a sensitivity analysis over a model
%    using the parallel-enabled simulation engines
%
%    SIMPLOT - plots simulation objects using syntax similar to
%    PLOT
%
%    SIMSWEEPDEMO(DEMO_MODELS) - provides examples on simsweep
%    options using files in the examples directory
%
%    SIMEXAMPLEPATH - returns the path for the installed example
%    files
%
%  See also: SIMEX, SIMSWEEP, SIMSENSITIVITY, SIMPLOT, SIMSWEEPDEMO


disp(' SIMHELP provides info on using the Simatra simEngine Dynamical ')
disp(' System Compiler in the Matlab environment ')
disp(' ')
disp(' Included commands: ')
disp('    SIMEX - executes the simEngine compiler using user supplied ')
disp('    input values (running with no arguments brings up a graphical ')
disp('    user interface) ')
disp('     ')
disp('    SIMSWEEP - executes the simEngine compiler multiple times ')
disp('    across a linear sweep of parameter space (running with no ')
disp('    arguments brings up a graphical user interface) ')
disp(' ')
disp('    SIMSENSITIVITY - executes a sensitivity analysis over a model ')
disp('    using the parallel-enabled simulation engines ')
disp(' ')
disp('    SIMPLOT - plots simulation objects using syntax similar to ')
disp('    PLOT ')
disp(' ')
disp('    SIMSWEEPDEMO(DEMO_MODELS) - provides examples on simsweep ')
disp('    options using files in the examples directory ')
disp(' ')
disp('    SIMEXAMPLEPATH - returns the path for the installed example ')
disp('    files ')
disp(' ')
disp('  See also: SIMEX, SIMSWEEP, SIMSENSITIVITY, SIMPLOT, SIMSWEEPDEMO ')

end