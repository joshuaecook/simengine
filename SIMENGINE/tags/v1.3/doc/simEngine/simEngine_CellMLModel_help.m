%% CellMLModel - simulate a CellML model using simEngine
%
%% Description
% The <matlab:doc('CellMLModel') CellMLModel> class provides a utility to
% convert CellML model descriptions into simEngine <simEngine_Model_help.html Model> objects.
% simEngine <simEngine_Model_help.html Model> objects can then be converted
% simulated with the <matlab:doc('Model.simex') simex> method.
%
% Currently, only a subset of CellML constructs are supported.  Those
% features that are not supported are units, reactions, and scoping.
%
%% Methods
%
% * <matlab:doc('CellMLModel.CellMLModel') CellMLModel> - create a
% simEngine model object from a CellML xml file
